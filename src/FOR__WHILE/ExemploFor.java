package FOR__WHILE;

public class ExemploFor {

    public static void main(String[] args) {

        // DELCARAR A VARIAVEL , CONDICAO E ATUALIZACAO

        for (int carneirinhos = 0; carneirinhos <= 20; carneirinhos++) {

            // COMANDO UTILIZADO ENQUANTO A CONDICAO FOR VERDADEIRA

            System.out.println(" contando carneirinhos " + carneirinhos);

        }

        //Verificando se a aplicacao funciona apos o termino do for

        System.out.println("Finalmente dormiu");
    }

}
